package ControlStatements;

public class Forloop8 {
	public static void main(String[] args) {
		int i;
		int j;
		int k;
		
		for(i=1;i<=4;i++)
		{
			for(j=1;j<=i;j++)
			{
				System.out.print(j);
				
			}
			for(k=1;k<=1;k++)
			{
				System.out.print(i);
			}
			System.out.println();
		}
	}

}
